package JsonObjects;

public class PlayerStatsJson
{
	private String Name;
	private int Score;

	public String getName()
	{
		return Name;
	}

	public void setName(String name)
	{
		Name = name;
	}

	public int getScore()
	{
		return Score;
	}

	public void setScore(int score)
	{
		Score = score;
	}
}
