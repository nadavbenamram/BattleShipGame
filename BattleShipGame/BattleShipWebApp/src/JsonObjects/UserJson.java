package JsonObjects;

public class UserJson
{
	private String Name;
	private int NumOfGames;

	public String getName()
	{
		return Name;
	}

	public void setName(String name)
	{
		Name = name;
	}

	public int getNumOfGames()
	{
		return NumOfGames;
	}

	public void setNumOfGames(int numOfGames)
	{
		NumOfGames = numOfGames;
	}
}
