package JsonObjects;

public class GameStatisticsJson
{
	private int Steps;
	private String GameDuration;

	public int getSteps()
	{
		return Steps;
	}

	public void setSteps(int steps)
	{
		Steps = steps;
	}

	public String getGameDuration()
	{
		return GameDuration;
	}

	public void setGameDuration(String gameDuration)
	{
		GameDuration = gameDuration;
	}
}

